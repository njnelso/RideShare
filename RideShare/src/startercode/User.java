package startercode;

public abstract class User {

	private String name;
	private String email;

    public String getName() {
        return name;
    }
    
    public void setName() {
        this.name = name;
    }
    
    public String getEmail() {
        return email;
    }
    
    public void setEmail() {
        this.email = email;
    }
}

